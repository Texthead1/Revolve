	public static class HatIDExtensions
	{
		public const int SSA_Low = 0;
		public const int SSA_High = 45;

		public const int Giants_Low = 46;
		public const int Giants_High = 96;

		public const int SwapForce_Low = 97;
		public const int SwapForce_High = 154;
		
		public const int TrapTeam_Low = 155;
		public const int TrapTeam_High = 255;

		public const int SuperChargers_Low = 256;
		public const int SuperChargers_High = 280;

		public static bool IsSSA(int hatID) => hatID.RangeInclusive(SSA_Low, SSA_High);
		public static bool IsGiants(int hatID) => hatID.RangeInclusive(Giants_Low, Giants_High);
		public static bool IsSwapForce(int hatID) => hatID.RangeInclusive(SwapForce_Low, SwapForce_High);
		public static bool IsTrapTeam(int hatID) => hatID.RangeInclusive(TrapTeam_Low, TrapTeam_High);
		public static bool IsSuperChargers(int hatID) => hatID.RangeInclusive(SuperChargers_Low, SuperChargers_High);
	}
